//Од тастатура се читаат непознат број на позитивни цели броеви (со максимум 5 цифри) се додека не се внесе нешто различно од број. За секој број треба да се најде позицијата (од десно на лево) на првото појавување на најголемата цифра во составот на бројот (цифрата на единици се наоѓа на позиција 0).
//Потоа, да се испечати статистика за позициите на најдените цифри со максимална вредност во следниот формат:
//[позиција]: [вкупно броеви чија максимална цифра се наоѓа на таа позиција]
//0: 2
//1: 3
//2: 1
//3: 1
//4: 1
//за броевите 97654 48654 12345 12343 1263 12443 12643 12777

#include <stdio.h>

int main() {
    int k, pos0=0, pos1=0, pos2=0, pos3=0, pos4=0;
    while (scanf("%d", &k)) {
        int kcpy = k;
        int max = kcpy % 10;
        while (kcpy > 0) {
            if (max < kcpy%10)
            {
                max = kcpy%10;
            }
            kcpy/=10;
        }
        //kcpy = k;
        if (max == kcpy%10)
        {
            pos0++;
            continue;
        }

        if (max == kcpy/10%10 && kcpy >=10)
        {
            pos1++;
            continue;
        }

        if (max == kcpy/100%10 && kcpy >=100)
        {
            pos2++;
            continue;
        }

        if (max == kcpy/1000%10 && kcpy>=1000)
        {
            pos3++;
            continue;
        }

        if (max == kcpy/10000 && kcpy >=10000)
        {
            pos4++;
            continue;
        }
    }

    printf("0: %d\n", pos0);
    printf("1: %d\n", pos1);
    printf("2: %d\n", pos2);
    printf("3: %d\n", pos3);
    printf("4: %d\n", pos4);
    return 0;

}